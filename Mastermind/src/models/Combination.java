package models;

public class Combination {

	private Pawn pawn1;
	private Pawn pawn2;
	private Pawn pawn3;
	private Pawn pawn4;
	
	public Combination() {
		
	}
	
	public Combination(Pawn pawn1, Pawn pawn2, Pawn pawn3, Pawn pawn4) {
		this.pawn1=pawn1;
		this.pawn2=pawn2;
		this.pawn3=pawn3;
		this.pawn4= pawn4;
	}
}
